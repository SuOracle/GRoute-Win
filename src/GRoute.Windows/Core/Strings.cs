    public static string LocalizeDigits(string text, Lang lang)
    {
        if (lang != Lang.Fa) return text;
        var sb = new StringBuilder(text.Length);
        foreach (var c in text) sb.Append(c is >= '0' and <= '9' ? FaDigits[c - '0'] : c);
        return sb.ToString();
    }

